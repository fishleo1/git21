#include <unistd.h>

void	ft_putstr(char *str)
{
	int		i;
	char	merge[3];

	i = 0;
	merge[0] = *str;
	merge[1] = '\0';
	while (merge[i] != '\0')
	{
		write(1, &merge[i], 1);
		i++;
	}
}
