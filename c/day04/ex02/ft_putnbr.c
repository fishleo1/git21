#include <unistd.h>
#include <stdio.h>

void	ft_putnbr(int nb)
{
	char ch_nb;

	if (nb == -2147483648)
	{
		write(1, &"-2147483648", 12);
	}
	if (nb < 0 && nb != -2147483648)
	{
		nb = nb * -1;
		write(1, "-", 1);
	}
	ch_nb = nb % 10 + '0';
	nb = nb / 10;
	if (nb != 0 && nb != -214748364)
	{
		ft_putnbr(nb);
	}
	if (nb != -214748364)
	{
		write(1, &ch_nb, 1);
	}
}
