int		ft_sqrt(int nb)
{
	int				res;
	float			x;
	float			y;
	float			e;

	x = nb;
	y = 1;
	e = 0.000001;
	while (x - y > e)
	{
		x = (x + y) / 2;
		y = nb / x;
	}
	res = (int)x;
	return (res);
}
